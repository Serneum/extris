defmodule Extris.GameTest do
  use ExUnit.Case
  alias Extris.Game
  alias Extris.Game.State

  test "The next piece becomes the current piece when the current piece hits the bottom" do
    state = %State{shape: :ell, next_shape: :oh, y: 17}
    new_state = Game.tick_game(state)
    assert new_state.shape == :oh
  end

  test "The board is included in the game state" do
    state = %State{}
    assert state.board == empty_board
  end

  test "The game knows how to overlay a shape on the board" do
    state = %State{shape: :ell, rotation: 0, x: 4, y: 0}
    assert Game.overlay_shape(state) == %State{state | board: board_with_ell}
  end

